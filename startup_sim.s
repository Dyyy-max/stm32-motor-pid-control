;=============================================================================
; 文件名: startup_sim.s
; 功能:   纯软件仿真启动文件（用于Keil仿真环境测试PID算法）
; 说明:   此文件为简化版启动代码，仅包含仿真所需的最基本初始化
;         不依赖真实STM32硬件，适用于算法验证和调试
;=============================================================================

;============================== 中断向量表 ==============================
                AREA    RESET, DATA, READONLY   ; 定义RESET段，数据属性，只读
                EXPORT  __Vectors               ; 导出向量表符号供链接器使用

__Vectors       DCD     0x20000400      ; 栈顶指针 (栈基址0x20000000，大小1KB)
                DCD     Reset_Handler   ; 复位中断向量（程序入口点）

;============================== 代码段 ==============================
                AREA    |.text|, CODE, READONLY  ; 定义代码段，只读

; 复位处理函数：系统启动后首先执行的代码
Reset_Handler   PROC
                EXPORT  Reset_Handler   [WEAK]  ; 导出符号，允许用户重定义
                IMPORT  main                    ; 导入main函数
                LDR     R0, =main               ; 加载main函数地址到R0
                BX      R0                      ; 跳转到main函数执行
                ENDP

;============================== 栈空间定义 ==============================
; 栈用于存储局部变量、函数调用返回地址等
                AREA    STACK, NOINIT, READWRITE, ALIGN=3
Stack_Size      EQU     0x00000400              ; 栈大小：1KB (0x400字节)
Stack_Mem       SPACE   Stack_Size              ; 分配栈空间

;============================== 堆空间定义 ==============================
; 堆用于动态内存分配（malloc等）
                AREA    HEAP, NOINIT, READWRITE, ALIGN=3
Heap_Size       EQU     0x00000400              ; 堆大小：1KB (0x400字节)
Heap_Mem        SPACE   Heap_Size               ; 分配堆空间

                END                             ; 文件结束标记